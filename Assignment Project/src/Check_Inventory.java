    public void loadModel() {
        try {
            String pdt_Name = cmb_pdt.getItemAt(cmb_pdt.getSelectedIndex());
            String query = "Select model from stock_mgt.products where pdt_Name = ? ";
            PreparedStatement ps = conn.prepareStatement(query);
            ps.setString(1, pdt_Name);

            String[] model = new String[6];

            ResultSet rs = ps.executeQuery();

            int count = 0;
            while (rs.next()) {
                model[count] = rs.getString("model");

                count++;
            }

            cmb_Model.removeAllItems();

            for (int i = 0; i < count; i++) {
                String item = model[i];
                cmb_Model.addItem(item);
            }

            ps.close();
            System.out.println("Combo loaded successfully!!!");
        } catch (Exception e) {
            System.out.println(e.toString());
        }
    }
