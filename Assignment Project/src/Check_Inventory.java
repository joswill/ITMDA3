    public void getAllProducts() {
        try {
            String query = "Select * from stock_mgt.products ";

            PreparedStatement ps = conn.prepareStatement(query);            
            ResultSet rs = ps.executeQuery();

            int count = 0;
            jta.append("pdt_ID" + "\t" + "pdt_Name" + "\t" + "pdt_Size" + "\t" + "pdt_Model" + "\t\t" + "pdt_Qty" + "\t" + "pdt_Price" + "\n");
            while (rs.next()) {
                jta.append(rs.getInt(1) + "\t" + rs.getString(2) + "\t" + rs.getInt(3) + "\t" + rs.getString(4) + "\t" + rs.getInt(5) + "\t" + rs.getDouble(6) + "\n");

                count++;
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    public void retrieveAll() {
        try {

            String name = cmb_pdt.getItemAt(cmb_pdt.getSelectedIndex());
            String model = cmb_Model.getItemAt(cmb_Model.getSelectedIndex());

            String query = "Select * from stock_mgt.products where pdt_Name = ? and model = ?";

            PreparedStatement ps = conn.prepareStatement(query);
            ps.setString(1, name);
            ps.setString(2, model);

            ResultSet rs = ps.executeQuery();

            int count = 0;
            jta.append("pdt_ID" + "\t" + "pdt_Name" + "\t" + "pdt_Size" + "\t" + "pdt_Model" + "\t\t" + "pdt_Qty" + "\t" + "pdt_Price" + "\n");
            while (rs.next()) {
                jta.append(rs.getInt(1) + "\t" + rs.getString(2) + "\t" + rs.getInt(3) + "\t" + rs.getString(4) + "\t" + rs.getInt(5) + "\t" + rs.getDouble(6) + "\n");

                count++;
            }
        } catch (Exception e) {
            System.out.println(e.toString());
        }
    }
